def prime_num() :
    '''
    $ prime numbers from 1 to 1000 : $
    '''
    for n in range(1,1001):
        counter = 0
        for i in range(2, ((n//2) + 1)):
            if n % i == 0 :
                counter = counter + 1
                break
        if (counter == 0 and n != 1):
            print(n, end = "\t")
            

print(prime_num.__doc__)

print("------------------------------------------------------------------------------------------------------------")
prime_num()
